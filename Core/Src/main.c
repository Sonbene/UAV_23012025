void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == huart2.Instance)
	{
		if(iBus_Check_CHKSUM(data_receive_ibus, 32) == 1)
		{
			iBus_Parsing(data_receive_ibus, &iBus);
			iBus.isReceived = 1;
		}

	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM11 && (motor_param.flyMode == FLY_MODE_ON || motor_param.flyMode == FLY_MODE_HOLD))
	{
		if(motor_param.flyMode == FLY_MODE_ON)
		{
			motor_param.PIDPitch.timesampling = motor_param.PIDRoll.timesampling = motor_param.PIDYaw.timesampling = 0.01;
			PIDControlAccel(&motor_param.PIDPitch, ICMData.pitch - ICMSetPoint.pitch, iBus.RH);
			PIDControlAccel(&motor_param.PIDRoll, ICMData.roll - ICMSetPoint.roll, iBus.RV);

			//motor_control(&htim1, &motor_param, iBus);
		}
		if(motor_param.flyMode == FLY_MODE_HOLD)
		{
			motor_param.PIDPitch.timesampling = motor_param.PIDRoll.timesampling = motor_param.PIDYaw.timesampling = 0.01;
			PIDControlAccel(&motor_param.PIDPitch, ICMData.pitch - ICMSetPoint.pitch, iBus.RH);
			PIDControlAccel(&motor_param.PIDRoll, ICMData.roll - ICMSetPoint.roll, iBus.RV);

			PIDControlAltitude(&motor_param.PIDAltitude, altitude, iBus.LV);
			//motor_control(&htim1, &motor_param, iBus);
		}
	}
}

void init_parameter_PID()
{
	//PID init
	PIDInit(&motor_param.PIDRoll, 1, 0, 0, 0.01);
	//PITCH
	//PIDInit(&PIDPitch,  0.0156, 0.0100, 0.0056, 0.01); //kp = 1, kd = 1, ki = 0.0040, timesampling = 0.04
	PIDInit(&motor_param.PIDPitch,  1, 0, 0, 1);
	//YAW
	//PIDInit(&PIDYaw, 0.125f, 0.0f, 0.000f, 0.01); //kp = 1, kd = 1, ki = 1, timesampling = 0.04
	PIDInit(&motor_param.PIDYaw, 1, 0.20, 0.0f, 1);
	//Altitude
	PIDInit(&motor_param.PIDAltitude, 1, 0.085, 0.00583, 0.01);
}

